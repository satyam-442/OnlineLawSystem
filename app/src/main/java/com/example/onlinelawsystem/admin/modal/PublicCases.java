package com.example.onlinelawsystem.admin.modal;

public class PublicCases {

    String PublicCaseName, PublicCaseAge, PublicCaseContact, PublicCaseAddress, PublicCaseDetail, PublicCaseExpertise, PublicCaseStatus, PublicCaseID;

    public PublicCases() {
    }

    public PublicCases(String publicCaseName, String publicCaseAge, String publicCaseContact, String publicCaseAddress, String publicCaseDetail, String publicCaseExpertise, String publicCaseStatus, String publicCaseID) {
        PublicCaseName = publicCaseName;
        PublicCaseAge = publicCaseAge;
        PublicCaseContact = publicCaseContact;
        PublicCaseAddress = publicCaseAddress;
        PublicCaseDetail = publicCaseDetail;
        PublicCaseExpertise = publicCaseExpertise;
        PublicCaseStatus = publicCaseStatus;
        PublicCaseID = publicCaseID;
    }

    public String getPublicCaseName() {
        return PublicCaseName;
    }

    public void setPublicCaseName(String publicCaseName) {
        PublicCaseName = publicCaseName;
    }

    public String getPublicCaseAge() {
        return PublicCaseAge;
    }

    public void setPublicCaseAge(String publicCaseAge) {
        PublicCaseAge = publicCaseAge;
    }

    public String getPublicCaseContact() {
        return PublicCaseContact;
    }

    public void setPublicCaseContact(String publicCaseContact) {
        PublicCaseContact = publicCaseContact;
    }

    public String getPublicCaseAddress() {
        return PublicCaseAddress;
    }

    public void setPublicCaseAddress(String publicCaseAddress) {
        PublicCaseAddress = publicCaseAddress;
    }

    public String getPublicCaseDetail() {
        return PublicCaseDetail;
    }

    public void setPublicCaseDetail(String publicCaseDetail) {
        PublicCaseDetail = publicCaseDetail;
    }

    public String getPublicCaseExpertise() {
        return PublicCaseExpertise;
    }

    public void setPublicCaseExpertise(String publicCaseExpertise) {
        PublicCaseExpertise = publicCaseExpertise;
    }

    public String getPublicCaseStatus() {
        return PublicCaseStatus;
    }

    public void setPublicCaseStatus(String publicCaseStatus) {
        PublicCaseStatus = publicCaseStatus;
    }

    public String getPublicCaseID() {
        return PublicCaseID;
    }

    public void setPublicCaseID(String publicCaseID) {
        PublicCaseID = publicCaseID;
    }
}